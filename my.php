    document.getElementById('downloadBtn').addEventListener('click', function() {
        // Mendapatkan data dari tabel
        var table = document.querySelector('.table');
        var rows = table.querySelectorAll('tr');

        // Inisialisasi string untuk menyimpan data CSV
        var csvContent = 'Nama,NIM,IPK1,IPK2,IPK3,IPK4,IPK5,IPK6,IPK7,IPK8,Prediksi SVM,Prediksi MLP\n';

        // Loop melalui setiap baris tabel, mulai dari indeks 1 untuk melewati baris header
        for (var i = 1; i < rows.length; i++) {
            var cells = rows[i].querySelectorAll('td');

            // Mendapatkan data dari setiap sel dan menambahkannya ke dalam string CSV
            var rowData = [];
            for (var j = 0; j < cells.length; j++) {
                rowData.push(cells[j].innerText);
            }
            csvContent += rowData.join(',') + '\n';
        }

        // Simpan CSV sebagai file
        var blob = new Blob([csvContent], {
            type: 'text/csv'
        });
        var link = document.createElement('a');
        link.href = window.URL.createObjectURL(blob);
        link.download = 'template_data_mahasiswa.csv';
        document.body.appendChild(link);
        link.click();
        document.body.removeChild(link);
    });
